SELECT
	warehouse.w_city,
	item.i_brand,
	inventory.inv_warehouse_sk,
	item.i_brand_id,
	inventory.inv_quantity_on_hand,
	warehouse.w_warehouse_sq_ft,
	warehouse.w_county,
	item.i_manager_id,
	item.i_class_id,
	item.i_item_desc,
	item.i_class,
	warehouse.w_warehouse_name,
	item.i_category,
	warehouse.w_country,
	item.i_size,
	item.i_item_id,
	item.i_current_price,
	item.i_manufact,
	item.i_units,
	inventory.inv_date_sk,
	item.i_item_sk,
	item.i_product_name,
	item.i_wholesale_cost,
	item.i_color,
	warehouse.w_warehouse_sk,
	item.i_category_id,
	inventory.inv_item_sk,
	item.i_manufact_id,
	warehouse.w_state
FROM
	inventory,
	warehouse,
	item
WHERE
	inventory.inv_warehouse_sk = warehouse.w_warehouse_sk
	AND inventory.inv_item_sk = item.i_item_sk